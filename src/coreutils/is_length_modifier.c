#include "libftprintf.h"

int		is_length_modifier(char *c)
{
	return ((*c == 'h' && *(c + 1) == 'h') || (*c == 'h') || (*c == 'l') ||
		(*c == 'l' && *(c + 1) == 'l') || (*c == 'j') ||
		(*c == 'z'));
}
