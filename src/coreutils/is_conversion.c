#include "libftprintf.h"

int		is_conversion(char c)
{
	return ((c == 's') || (c == 'S') || (c == 'p') || (c == 'd') ||
		(c == 'D') || (c == 'i') || (c == 'o') || (c == 'O') || (c == 'u') ||
		(c == 'U') || (c == 'x') || (c == 'X') || (c == 'c') || (c == 'C')
		|| (c == '%'));
}
